# ----------------------------- Data type: 숫자 ---------------------------- # 
print(5)
print(-10)
print(1000)
print(5+3)
print(2*8)
print(3*(3+1))


# ----------------------------- Data type: 문자열 ----------------------------- # 
print('풍선')
print("butterfly")
print("ㅋ"*9)  # 문자열과 정수 계산해서 출력 가능

sentence = "나는 소년입니다."
print(sentence)
sentence2 = 'Python is easy'
print(sentence2)
sentence3 = '''
I am a boy and 
Python is easy
'''
print(sentence3)


# ----------------------------- Data type: Boolean ----------------------------- # 
print(5 > 10)
print(5 < 10)
print(True)
print(False)
print(not True)
print(not False)
print(not(5<10))
